std::tuple<double, double> CoordinateSystemUtilsCoeff::getPolarPerturbations(double jd)
{
    /* The database and the prediction model are extracted from the [IERS Bulletin-A]
     * (http://maia.usno.navy.mil/ser7/ser7.dat). The x_p and y_p correspond to the polar
     * perturbations in arc-second. The A and C values are computed to be used in the prediction
     * model
     **/
    double x_p;
    double y_p;
    double A;
    double C;
    unsigned int i;
    double mjd = jd - 2400000.5;

    for(i = 0; i < (IERS_DATABASE_COUNT / 4); i++) {
        if(mjd < iers_database[i * 4]) {
            if(i > 0) {
                x_p = MathUtils::arcsecToRad(iers_database[(i - 1) * 4 + 1]);
                y_p = MathUtils::arcsecToRad(iers_database[(i - 1) * 4 + 2]);
            } else {
                x_p = MathUtils::arcsecToRad(iers_database[i * 4 + 1]);
                y_p = MathUtils::arcsecToRad(iers_database[i * 4 + 2]);
            }
            break;
        }
    }

    if(i == (IERS_DATABASE_COUNT / 4)) {
        /* Not enough database -> using prediction model (innacurate) */
        A = 2 * Config::pi * (jd - 58157) / 365.25;
        C = 2 * Config::pi * (jd - 58157) / 435;
        x_p = 0.1175 - 0.1088 * cos(A) - 0.0795 * sin(A) - 0.0064 * cos(C) + 0.0221 * sin(C);
        x_p = MathUtils::arcsecToRad(x_p);
        y_p = 0.3531 - 0.0742 * cos(A) + 0.0994 * sin(A) + 0.0221 * cos(C) + 0.0064 * sin(C);
        y_p = MathUtils::arcsecToRad(y_p);
    }

    return std::make_tuple(x_p, y_p);
}

